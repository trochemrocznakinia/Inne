from CBWG.SCH_SPRAWY_KREDYTOWE.T_SPRAWY_KREDYTOWE as skre  
JOIN CBWG.[SCH_SPRAWY_KREDYTOWE].[T_WERSJE_OFERT_KREDYTOW] as WOK on WOK.[WOK_SKRE_ID]=skre.[SKRE_ID]
JOIN CBWG.[SCH_SPRAWY_KREDYTOWE].[T_OFERTY_KREDYTOWE] as OfKred on OfKred.[OKRE_WOK_ID]=WOK.[WOK_ID]
