JOIN CBWG.SCH_KLIENCI.T_WERSJE_DANYCH_UCZESTNIKOW as wduc on wduc.WDUC_USKR_ID = uskr.USKR_ID 
JOIN CBWG.[SCH_KLIENCI].[T_DANE_OSOBOWE] as doso on wduc.[WDUC_DOSO_ID]=doso.[DOSO_ID] 
JOIN CBWG.[SCH_KLIENCI].[T_DANE_POZOSTALE] as dpoz on wduc.[WDUC_DPOZ_ID]=dpoz.[DPOZ_ID] 
join CBO.[SCH_OFERTY].[T_OFERTY] as ofer on ofer.OFER_ID = OfKred.[OKRE_CBO_OFER_ID]


